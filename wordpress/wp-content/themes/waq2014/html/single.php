                            <header class="content-header">
                                <h1 itemprop="name">Ubiquitous Information Architecture: A Framework for Cross-Channel Strategy</h1>
                                <div class="informations">
                                    <span class="session-speaker" itemprop="performer" itemscope itemtype="http://schema.org/Person">
                                        <span itemprop="name">Peter Morville</span>
                                    </span>
                                    <span class="session-meta">
                                        <span class="date"><time datetime="2014-02-23">Jeudi 23 février</time></span>
                                        <span class="time">
                                            <time itemprop="startDate" datetime="2014-02-23 08:30">8 h 30</time> à
                                            <time itemprop="endDate" datetime="2014-02-23 10:00">10 h 00</time>
                                        </span>
                                        <span class="location" itemprop="location" itemscope itemtype="http://schema.org/Place">
                                            <span itemprop="address" itemscope itemtype="http://schema.org/Place">
                                                <span itemprop="name">Grand hall</span>
